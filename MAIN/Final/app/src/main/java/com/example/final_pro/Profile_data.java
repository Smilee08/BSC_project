package com.example.final_pro;

public class Profile_data {
    String name,about,phone,email;

    public Profile_data() {
    }


    public Profile_data(String name, String about, String phone, String email) {
        this.name = name;
        this.about = about;
        this.phone = phone;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
