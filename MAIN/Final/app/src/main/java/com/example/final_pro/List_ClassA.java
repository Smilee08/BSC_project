package com.example.final_pro;

public class List_ClassA {
    private String room;
    private String name;

    public List_ClassA() {
    }

    public List_ClassA(String room, String name) {
        this.room = room;
        this.name = name;
    }

    public String getRoom() {
        return room;
    }

    public void setRoom(String room) {
        this.room = room;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
